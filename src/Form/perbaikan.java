    private void tbl_perbaikanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_perbaikanMouseClicked
       int bar = tbl_perbaikan.getSelectedRow();
        if (bar >= 0 && tabmodeperbaikan.getRowCount() > 0) {
        String id_perbaikan = tabmodeperbaikan.getValueAt(bar, 0).toString();
        String nama_a = tabmodeperbaikan.getValueAt(bar, 1).toString();
        String kode_a = tabmodeperbaikan.getValueAt(bar, 2).toString();
        String tgl_per = tabmodeperbaikan.getValueAt(bar, 3).toString();
        String des = tabmodeperbaikan.getValueAt(bar, 4).toString();
        String stat = tabmodeperbaikan.getValueAt(bar, 5).toString();
        String nam_pel = tabmodeperbaikan.getValueAt(bar, 6).toString();
        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsedDatePnjm = sdf.parse(tgl_per);
            tgl_permintaan.setDate(parsedDatePnjm);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Tanggal tidak bisa dibaca: " + e.getMessage());
            tgl_permintaan.setDate(null); 
        }

        id_aset.setText(kode_a);
        nama_aset.setText(nama_a);
        deskripsi.setText(des);
        status.setText(stat);
        pelapor.setText(nam_pel);
        txtHiddenId.setText(id_perbaikan);
        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih atau tabel kosong.");
        }
    }//GEN-LAST:event_tbl_perbaikanMouseClicked


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode_asset = id_aset.getText();
        String cekSql = "SELECT * FROM data_asset WHERE kode_asset =?";
        
        Date selectedDatePnjm = tgl_permintaan.getDate();
        
        try{
            // Cek apakah kode_asset valid
            PreparedStatement cekStat = conn.prepareStatement(cekSql);
            cekStat.setString(1, kode_asset);
            ResultSet rs = cekStat.executeQuery();

            if (!rs.next()) {
                // Jika tidak ditemukan
                JOptionPane.showMessageDialog(null, "Kode asset tidak valid. Data tidak disimpan.");
                return; 
            }
            
            String sql = "INSERT INTO perbaikan_asset(nama_asset, id_asset, tgl_perbaikan, deskripsi, status, nama_pelapor) VALUES (?,?,?,?,?,?)";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nama_aset.getText());
            stat.setString(2, id_aset.getText());            
            if (selectedDatePnjm != null) {
                java.sql.Date sqlDate = new java.sql.Date(selectedDatePnjm.getTime());
                stat.setDate(3, sqlDate);
            } else {
                stat.setNull(3, java.sql.Types.DATE);
            }
           
            stat.setString(4, deskripsi.getText());
            stat.setString(5, status.getText());
            stat.setString(6, pelapor.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            nama_aset.requestFocus();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"data gagal disimpan "+ex);
        }
        datatableAset();
        datatablePerbaikan();
    }//GEN-LAST:event_btnSimpanActionPerformed


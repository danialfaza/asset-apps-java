    protected void datatable(){
        Object[] Baris ={"id","Nama Peminjam","Kode Asset","Nama Asset","Tgl Pinjam", "Tgl Pengembalian", "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
            
        try{
                String sql = "SELECT id, nama_peminjam, p.kode_asset, d.nama, tgl_pinjam, tgl_pengembalian, keterangan FROM peminjaman as p JOIN data_asset as d ON p.kode_asset=d.kode_asset ";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                
                while(hasil.next()){
                    tabmode.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7),
                    });
                }
                table.setModel(tabmode);
                table.getColumnModel().getColumn(0).setMinWidth(0);
                table.getColumnModel().getColumn(0).setMaxWidth(0);
                table.getColumnModel().getColumn(0).setWidth(0);
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,"data gagal diload "+ex);
            }    
            
    }


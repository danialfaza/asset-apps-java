    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int bar = table.getSelectedRow();
        String id = tabmode.getValueAt(bar, 0).toString();
        String nm_pmnjm = tabmode.getValueAt(bar, 1).toString();
        String kd_ast = tabmode.getValueAt(bar, 2).toString();
        String pinjam = tabmode.getValueAt(bar, 4).toString();
        String pengembalian = tabmode.getValueAt(bar, 5).toString();
        String ket= tabmode.getValueAt(bar, 6).toString();
        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsedDatePnjm = sdf.parse(pinjam);
            java.util.Date parsedDatePgmbln = sdf.parse(pengembalian);
            tgl_pnjm.setDate(parsedDatePnjm);
            tgl_pgmbln.setDate(parsedDatePgmbln);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Tanggal tidak bisa dibaca: " + e.getMessage());
            tgl_pnjm.setDate(null); 
            tgl_pgmbln.setDate(null); 
        }
        
        nama.setText(nm_pmnjm);
        kode.setText(kd_ast);
        keterangan.setText(ket);
        txtHiddenId.setText(id);
       
    }//GEN-LAST:event_tableMouseClicked


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode_asset = kode.getText();
        String cekSql = "SELECT * FROM data_asset WHERE kode_asset =?";
        
        Date selectedDatePnjm = tgl_pnjm.getDate();
        Date selectedDatePgmbln = tgl_pgmbln.getDate();
        
        try{
            // Cek apakah kode_asset valid
            PreparedStatement cekStat = conn.prepareStatement(cekSql);
            cekStat.setString(1, kode_asset);
            ResultSet rs = cekStat.executeQuery();

            if (!rs.next()) {
                // Jika tidak ditemukan
                JOptionPane.showMessageDialog(null, "Kode asset tidak valid. Data tidak disimpan.");
                return; 
            }
            
            String sql = "INSERT INTO peminjaman(nama_peminjam, kode_asset, tgl_pinjam, tgl_pengembalian, keterangan) VALUES (?,?,?,?,?)";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nama.getText());
            stat.setString(2, kode.getText());            
            if (selectedDatePnjm != null) {
                java.sql.Date sqlDate = new java.sql.Date(selectedDatePnjm.getTime());
                stat.setDate(3, sqlDate);
            } else {
                stat.setNull(3, java.sql.Types.DATE);
            }
            if (selectedDatePgmbln != null) {
                java.sql.Date sqlDate = new java.sql.Date(selectedDatePgmbln.getTime());
                stat.setDate(4, sqlDate);
            } else {
                stat.setNull(4, java.sql.Types.DATE);
            }
            stat.setString(5, keterangan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            kode.requestFocus();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"data gagal disimpan "+ex);
        }
        datatable();
    }//GEN-LAST:event_btnSimpanActionPerformed

